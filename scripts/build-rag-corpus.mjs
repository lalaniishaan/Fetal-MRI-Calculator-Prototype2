import fs from "fs/promises";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const rootDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = join(rootDir, "src", "rag-corpus.generated.ts");

const documents = [
  { sourceId: "SPEC", title: "Fetal MRI Biometry Calculator Specification", path: "SPEC.md" },
  { sourceId: "TEST", title: "Validation Cases", path: "TEST.md" },
  { sourceId: "PLAN", title: "Implementation Plan", path: "PLAN.md" },
  { sourceId: "PROGRESS", title: "Implementation Progress", path: "PROGRESS.md" }
];

const embeddedDocuments = await Promise.all(
  documents.map(async (document) => ({
    ...document,
    text: await fs.readFile(join(rootDir, document.path), "utf-8")
  }))
);

const body = [
  "import type { RagDocument } from \"./rag.js\";",
  "",
  "// Generated by scripts/build-rag-corpus.mjs. Do not edit by hand.",
  "export const embeddedRagDocuments: RagDocument[] = ",
  JSON.stringify(embeddedDocuments, null, 2),
  ";",
  ""
].join("\n");

await fs.writeFile(outputPath, body);
